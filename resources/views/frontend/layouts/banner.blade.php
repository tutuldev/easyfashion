<script src="https://cdn.jsdelivr.net/npm/swiper@11/swiper-bundle.min.js"></script>
<script>
  const swiper = new Swiper(".swiper", {
    loop: true,
    autoplay: {
      delay: 7000,
      disableOnInteraction: false,
    },
    pagination: {
      el: ".swiper-pagination",
      clickable: true,
    },
    on: {
      init: function() {
        // On Swiper initialization (page load), hide the overlay layer for the first slide
        const firstOverlay = this.slides[this.activeIndex].querySelector('.overlay');
        if (firstOverlay) {
          firstOverlay.style.display = 'none'; // Initially hide the white overlay
        }

        // And set the first blur layer to its initial 80% left state
        const firstBlurLayer = this.slides[this.activeIndex].querySelector('.blur-layer');
        if (firstBlurLayer) {
          firstBlurLayer.classList.add('initial-state');
        }
      },
      slideChangeTransitionStart() {
        const currentSlide = swiper.slides[swiper.activeIndex];
        const currentOverlay = currentSlide.querySelector('.overlay');
        const currentBlurLayer = currentSlide.querySelector('.blur-layer');

        // Reset all overlay and blur layers for the upcoming animation
        document.querySelectorAll('.overlay').forEach(el => {
          el.classList.remove('overlay-slide');
          el.style.transform = 'translateX(0)';
          el.style.display = 'block'; // Ensure overlay is visible for animation
        });
        document.querySelectorAll('.blur-layer').forEach(el => {
          el.classList.remove('blur-enter', 'blur-slide-left', 'initial-state');
          el.style.transform = 'translateX(0) translateZ(0)';
          el.style.opacity = '0';
        });

        // Start white layer animation
        if (currentOverlay) {
          void currentOverlay.offsetWidth; // Trigger reflow
          currentOverlay.classList.add('overlay-slide');
        }

        // Start blur layer "enter" animation (opacity fade in)
        if (currentBlurLayer) {
          void currentBlurLayer.offsetWidth; // Trigger reflow
          currentBlurLayer.classList.add('blur-enter');
        }
      },
      slideChangeTransitionEnd() {
        const currentSlide = swiper.slides[swiper.activeIndex];
        const currentOverlay = currentSlide.querySelector('.overlay');
        const currentBlurLayer = currentSlide.querySelector('.blur-layer');

        // After white layer animation completes, start blur layer's slide-left animation
        if (currentOverlay && currentBlurLayer) {
          currentOverlay.addEventListener('animationend', function handler() {
            currentOverlay.removeEventListener('animationend', handler);
            currentBlurLayer.style.opacity = '1';
            currentBlurLayer.classList.add('blur-slide-left');
          }, {
            once: true
          });
        }
      }
    }
  });

  // Custom navigation button handlers
  document.getElementById("custom-prev").addEventListener("click", () => {
    swiper.slidePrev();
  });
  document.getElementById("custom-next").addEventListener("click", () => {
    swiper.slideNext();
  });
</script>
@endpush
